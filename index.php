<div class = "block_1">
    <img src = "IMG/pic_2.png"> В обстановке наступления войск германского империализма на Советскую Россию 22 февраля был опубликован декрет-воззвание СНК "Социалистическое Отечество в опасности!" (принят 21 февраля), а 23 февраля состоялись массовые митинги в Петрограде (ныне Санкт-Петербург), Москве и других городах страны, на которых трудящихся призывали встать на защиту своего Отечества от наступавших германских войск. Этот день ознаменовался массовым вступлением добровольцев в Красную Армию и началом формирования ее отрядов и частей.
</div>

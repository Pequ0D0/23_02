    <img src = "IMG/pic_1.png">23 февраля в России отмечается День защитника Отечества, являющейся днем воинской славы страны.
Праздник зародился еще в СССР; тогда 23 февраля ежегодно отмечался как всенародный праздник – День Советской Армии и Военно-Морского Флота.
Документа, учреждавшего 23 февраля как официальный советский праздник, не существовало. Советская историография связывала приурочивание чествования военных к этой дате с событиями 1918 года: 28 января (15 января по старому стилю) 1918 года Совет народных комиссаров (СНК) во главе с председателем Владимиром Лениным принял Декрет об организации Рабоче-Крестьянской Красной Армии (РККА), а 11 февраля (29 января по старому стилю) – Рабоче-Крестьянского Красного Флота (РККФ).
</div>

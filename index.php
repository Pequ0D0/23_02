<div class = "block_1">
    <img src = "IMG/pic_3.png">10 января 1919 года председатель Высшей военной инспекции РККА Николай Подвойский направил в президиум Всероссийского центрального исполнительного комитета (ВЦИК) предложение отпраздновать годовщину создания Красной Армии, приурочив торжество к ближайшему воскресенью до или после 28 января. Однако из-за позднего предоставления ходатайства решение не было принято.
Тогда инициативу празднования первой годовщины РККА взял на себя Моссовет. 24 января 1919 года его президиум, возглавляемый Львом Каменевым, постановил приурочить эти торжества ко дню Красного подарка, проводимого с целью сбора материальных и денежных средств для Красной Армии. При ВЦИК был создан Центральный комитет по организации празднования годовщины Красной Армии и дня Красного подарка, который назначил проведение торжеств на воскресенье 23 февраля. 5 февраля 1919 года "Правда" и другие газеты опубликовали сообщение: "Устройство дня Красного подарка по всей России перенесено на 23 февраля. В этот день по городам и на фронте будет организовано празднование годовщины создания Красной армии, исполнившейся 28 января".
